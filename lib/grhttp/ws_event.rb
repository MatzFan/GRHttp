require 'stringio'
module GRHttp

	class WSEvent
		attr_reader :io, :data

		def initialize io, data = nil
			@io = io
			@data = data
		end

		def write data
			# should synchronize?
			# @io.locker.synchronize { ... } 
			@io.send Base::WSHandler.frame_data(@io, data.to_s) if data
		end
		alias :send :write
		alias :<< :write
		def close
			@io.send( CLOSE_FRAME )
			@io.close
		end
		alias :disconnect :close
		def ping
			@io.send( PING_FRAME )
		end
		def pong
			@io.send( PONG_FRAME )
		end

		protected
		PONG_FRAME = "\x8A\x00".freeze
		PING_FRAME = "\x89\x00".freeze
		CLOSE_FRAME = "\x88\x00".freeze
	end
end


